function getMessage(messageLarge) {
  var temp = messageLarge;
  switch (temp) {
    case "Hi":
      return "Sup man";
      break;
    case "Bye":
      return "Bye man, you wont be missed HOHOHOHOHOHOHOHOHOHOHO!";
      break;
    default:
      return "I dont know what are you talking about :( ";
      break;
  }
}
